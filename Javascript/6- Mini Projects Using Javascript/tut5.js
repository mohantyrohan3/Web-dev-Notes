let now = new Date('June 24 2003');
// console.log(now);

console.log(now.getDate()); // Getting date (0 to 31 maybe)

console.log(now.getFullYear()); // Getting Year

console.log(now.getDay()); // Getting day 

console.log(now.getMonth()); // Getting Month

console.log(now.toDateString()); // Readable Format


